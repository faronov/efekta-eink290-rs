pub struct Paint {
    pub buf: [u8; BUF_SIZE],
}

impl Paint {
    /// Create a new paint buffer, filled white.
    pub fn new() -> Self {
        Self {
            buf: [0xFF; BUF_SIZE],
        }
    }

    /// Fill the entire buffer with a colour.
    pub fn clear(&mut self, color: u8) {
        let fill = if color == BLACK { 0x00 } else { 0xFF };
        self.buf.fill(fill);
    }

    /// Set a single pixel. `color`: 0 = black, 1 = white.
    pub fn pixel(&mut self, x: u16, y: u16, color: u8) {
        if x >= WIDTH || y >= HEIGHT {
            return;
        }
        let idx = ((y as usize) * (WIDTH as usize) + (x as usize)) / 8;
        let bit = 7 - ((x as usize) % 8);
        if color == BLACK {
            self.buf[idx] &= !(1 << bit);
        } else {
            self.buf[idx] |= 1 << bit;
        }
    }

    /// Horizontal line.
    pub fn hline(&mut self, x: u16, y: u16, w: u16, color: u8) {
        for dx in 0..w {
            self.pixel(x + dx, y, color);
        }
    }

    /// Vertical line.
    #[allow(dead_code)]
    pub fn vline(&mut self, x: u16, y: u16, h: u16, color: u8) {
        for dy in 0..h {
            self.pixel(x, y + dy, color);
        }
    }

    /// Filled rectangle.
    #[allow(dead_code)]
    pub fn fill_rect(&mut self, x: u16, y: u16, w: u16, h: u16, color: u8) {
        for dy in 0..h {
            self.hline(x, y + dy, w, color);
        }
    }

    /// Render one character, returns the X advance (char width + 1 gap).
    pub fn draw_char(&mut self, x: u16, y: u16, ch: u8, font: &Font, color: u8) -> u16 {
        if ch < font.first_char || ch > font.last_char {
            return font.width + 1;
        }
        let offset = (ch - font.first_char) as usize * font.width as usize;
        for col in 0..font.width {
            let bits = font.data[offset + col as usize];
            for row in 0..font.height {
                if bits & (1 << row) != 0 {
                    self.pixel(x + col, y + row, color);
                } else {
                    // Draw background as opposite colour for clean text
                    self.pixel(x + col, y + row, color ^ 1);
                }
            }
        }
        font.width + 1
    }

    /// Render a string.
    pub fn draw_str(&mut self, x: u16, y: u16, s: &str, font: &Font, color: u8) {
        let mut cx = x;
        for ch in s.bytes() {
            cx += self.draw_char(cx, y, ch, font, color);
        }
    }
}
