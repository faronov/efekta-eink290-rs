//! Minimal frame-buffer painter for 128×296 monochrome e-paper.
//!
//! Bit layout: MSB-first, 1 = white, 0 = black.
//! Byte index = (y * WIDTH + x) / 8, bit = 7 - (x % 8).

use crate::ssd1680::{BUF_SIZE, HEIGHT, WIDTH};

pub const BLACK: u8 = 0;
pub const WHITE: u8 = 1;

/// 5×7 bitmap font covering ASCII 0x20–0x7E (95 glyphs, 5 bytes each).
/// Each byte is a column, LSB = top row.
pub struct Font {
    pub data: &'static [u8],
    pub width: u16,
    pub height: u16,
    pub first_char: u8,
    pub last_char: u8,
}

